           --set spotify_next icon=􀊐 \
           --set spotify_next icon.color=$WHITE \
           --set spotify_next icon.font="SF Pro:Regular:14.0" \
           --set spotify_next icon.padding_left=3 \
           --set spotify_next icon.padding_right=5 \
           --set spotify_next click_script="$PLUGIN_DIR/spotify_next_click.sh" \
           --subscribe spotify_indicator spotify_change \
           --subscribe spotify_prev spotify_change \
           --subscribe spotify_play spotify_change \
           --subscribe spotify_next spotify_change \
           --update spotify_indicator \
           --update spotify_play