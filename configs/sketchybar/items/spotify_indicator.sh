           --set spotify_indicator label.drawing=on \
           --set spotify_indicator label.padding_right=5 \
           --set spotify_indicator update_freq=2 \
           --set spotify_indicator script="$PLUGIN_DIR/spotify_indicator.sh" \
           --set spotify_indicator click_script="osascript -e 'tell application \"Spotify\" to activate'" \
           --add item spotify_prev center \
           --set spotify_prev drawing=on \
           --set spotify_prev icon.drawing=on \
           --set spotify_prev label.drawing=off \
           --set spotify_prev background.drawing=off \
           --set spotify_prev icon=􀊎 \
           --set spotify_prev icon.color=$WHITE \
           --set spotify_prev icon.font="SF Pro:Regular:14.0" \
           --set spotify_prev icon.padding_left=5 \
           --set spotify_prev icon.padding_right=3 \
           --set spotify_prev click_script="$PLUGIN_DIR/spotify_prev_click.sh" \
           --add item spotify_play center \
           --set spotify_play drawing=on \
           --set spotify_play icon.drawing=on \
           --set spotify_play label.drawing=off \
           --set spotify_play background.drawing=off \
           --set spotify_play icon=􀊄 \
