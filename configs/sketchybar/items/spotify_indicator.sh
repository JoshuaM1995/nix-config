           --set spotify_play icon.font="SF Pro:Regular:14.0" \
           --set spotify_play icon.padding_left=3 \
           --set spotify_play icon.padding_right=3 \
           --set spotify_play script="$PLUGIN_DIR/spotify_play.sh" \
           --set spotify_play update_freq=2 \
           --set spotify_play click_script="$PLUGIN_DIR/spotify_play_click.sh" \
           --add item spotify_next center \
           --set spotify_next drawing=on \
